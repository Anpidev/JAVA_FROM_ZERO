package bucles;

/**
 * Ejercicio 2
 * Muestra los números múltiplos de 5 de 0 a 100 utilizando un bucle while.
 * 
 * @author angelaruiz
 */
public class Bucles2 {
  public static void main(String[] args) {
    int numero = 0;
    while (numero <= 100) {
      System.out.println(numero);
      numero += 5;

    }

  }
}
